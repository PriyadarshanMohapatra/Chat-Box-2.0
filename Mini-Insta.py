@app.route("/friends", methods=["GET", "POST"])
@login_required
def friends():
    me = session["username"]
    conn = get_db()
    notice = ""
    notice_is_error = False

    if request.method == "POST":
        target = request.form.get("friend_username", "").strip().lower()
        if not target:
            notice = "Please enter a username."
            notice_is_error = True
        elif target == me:
            notice = "You can't add yourself."
            notice_is_error = True
        else:
            exists = conn.execute(
                "SELECT username FROM users WHERE username = ?", (target,)
            ).fetchone()
            if not exists:
                notice = f'No user found with the ID "{target}".'
                notice_is_error = True
            else:
                a, b = friend_pair(me, target)
                existing = conn.execute(
                    "SELECT * FROM friendships WHERE user_a = ? AND user_b = ?", (a, b)
                ).fetchone()
                if existing:
                    notice = "You're already friends, or a request is already pending."
                    notice_is_error = True
                else:
                    conn.execute(
                        "INSERT INTO friendships (user_a, user_b, status, requested_by) "
                        "VALUES (?, ?, 'pending', ?)",
                        (a, b, me),
                    )
                    conn.commit()
                    notice = f'Friend request sent to "{target}".'

    accepted_rows = conn.execute(
        "SELECT * FROM friendships WHERE (user_a = ? OR user_b = ?) AND status = 'accepted'",
        (me, me),
    ).fetchall()
    friend_list = [r["user_b"] if r["user_a"] == me else r["user_a"] for r in accepted_rows]

    incoming_rows = conn.execute(
        "SELECT * FROM friendships WHERE (user_a = ? OR user_b = ?) "
        "AND status = 'pending' AND requested_by != ?",
        (me, me, me),
    ).fetchall()
    incoming = [r["user_b"] if r["user_a"] == me else r["user_a"] for r in incoming_rows]

    outgoing_rows = conn.execute(
        "SELECT * FROM friendships WHERE (user_a = ? OR user_b = ?) "
        "AND status = 'pending' AND requested_by = ?",
        (me, me, me),
    ).fetchall()
    outgoing = [r["user_b"] if r["user_a"] == me else r["user_a"] for r in outgoing_rows]

    conn.close()

    def friend_row(name):
        safe = html_lib.escape(name)
        room = friend_room(me, name)
        return f"""
        <li class="content-block" style="display:flex; justify-content:space-between; align-items:center; flex-wrap:wrap; gap:0.5rem;">
            <strong>{safe}</strong>
            <span>
                <a href="/chat/{safe}" class="btn">Chat</a>
                <a href="/call?room={room}" class="btn">Call</a>
                <a href="/video-call?room={room}" class="btn">Video Call</a>
            </span>
        </li>
        """

    def incoming_row(name):
        safe = html_lib.escape(name)
        return f"""
        <li class="content-block" style="display:flex; justify-content:space-between; align-items:center;">
            <strong>{safe}</strong>
            <span>
                <form method="POST" action="/friends/accept/{safe}" style="display:inline;">
                    <button type="submit" class="btn">Accept</button>
                </form>
                <form method="POST" action="/friends/decline/{safe}" style="display:inline;">
                    <button type="submit" class="btn btn-danger">Decline</button>
                </form>
            </span>
        </li>
        """

    friend_items = "".join(friend_row(f) for f in friend_list) or "<p>No friends yet, add one below.</p>"
    incoming_items = "".join(incoming_row(f) for f in incoming) or "<p>No incoming requests.</p>"
    outgoing_items = (
        "".join(f"<li>{html_lib.escape(f)} (waiting for them to accept)</li>" for f in outgoing)
        or "<p>None right now.</p>"
    )

    notice_class = "flash-error" if notice_is_error else "flash-success"
    notice_html = f'<div class="flash {notice_class}">{html_lib.escape(notice)}</div>' if notice else ""

    body = f"""
    <section class="content-block">
        <h1>My ID: {html_lib.escape(me)}</h1>
        <p>Share this ID with friends so they can add you.</p>
    </section>

    <section class="content-block">
        <h2>Add a Friend</h2>
        {notice_html}
        <form method="POST" action="/friends">
            <input type="text" name="friend_username" placeholder="Friend's ID">
            <button type="submit" class="btn">Send Request</button>
        </form>
    </section>

    <section class="content-block">
        <h2>Incoming Requests</h2>
        <ul style="list-style:none; padding:0;">{incoming_items}</ul>
    </section>

    <section class="content-block">
        <h2>Sent Requests</h2>
        <ul>{outgoing_items}</ul>
    </section>

    <section>
        <h2>My Friends</h2>
        <ul style="list-style:none; padding:0;">{friend_items}</ul>
    </section>
    """
    return layout("Friends", body)


