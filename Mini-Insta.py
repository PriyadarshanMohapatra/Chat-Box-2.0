@socketio.on("chat_message")
def on_chat_message(data):
    room = (data or {}).get("room", "")
    sender = (data or {}).get("sender", "")
    content = (data or {}).get("content", "").strip()
    if not room or not sender or not content:
        return

    conn = get_db()
    conn.execute(
        "INSERT INTO chat_messages (room, sender, content, created_at) VALUES (?, ?, ?, datetime('now'))",
        (room, sender, content),
    )
    conn.commit()
    conn.close()

    emit("chat_message", {"sender": sender, "content": content}, room=room)


