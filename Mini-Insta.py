@socketio.on("join")
def on_join(data):
    room = (data or {}).get("room", "").strip()
    if not room:
        return

    join_room(room)
    members = add_to_room(room, request.sid)
    count = len(members)

    if count == 1:
        emit("status", {"msg": "Waiting for someone else to join..."})
    elif count == 2:
        caller_sid, callee_sid = members[0], members[1]
        emit("ready", {"initiator": True}, room=caller_sid)
        emit("ready", {"initiator": False}, room=callee_sid)
    else:
        emit("status", {"msg": "Room is full (2 people max). Try a different code."})
        leave_room(room)
        remove_from_room(room, request.sid)


@socketio.on("offer")
def on_offer(data):
    emit("offer", data, room=data["room"], include_self=False)


@socketio.on("answer")
def on_answer(data):
    emit("answer", data, room=data["room"], include_self=False)


@socketio.on("ice-candidate")
def on_ice_candidate(data):
    emit("ice-candidate", data, room=data["room"], include_self=False)


@socketio.on("leave")
def on_leave(data):
    room = (data or {}).get("room", "")
    if not room:
        return
    leave_room(room)
    remove_from_room(room, request.sid)
    emit("peer-left", {}, room=room)


