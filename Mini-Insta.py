# Tracks who is currently in each call room: { "room-code": [sid1, sid2] }
rooms = {}


def add_to_room(room, sid):
    members = rooms.setdefault(room, [])
    if sid not in members:
        members.append(sid)
    return members


def remove_from_room(room, sid):
    if room in rooms and sid in rooms[room]:
        rooms[room].remove(sid)
        if not rooms[room]:
            del rooms[room]


